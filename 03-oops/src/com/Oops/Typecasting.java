package com.Oops;

public class Typecasting {
    public static void main(String[] args) {

        int i = 10;
        double d = i;   
        System.out.println("Widening (int to double): " + d);

        double x = 12.9;
        int y = (int) x;  
        System.out.println("Narrowing (double to int): " + y);

      

      
    }
}

